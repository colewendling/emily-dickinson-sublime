export interface Poem {
  number: number;
  date: number;
  lineCount: number;
  title: string;
  content: string[];
  themes: string[];
  // Extra properties for 3D visualization
  position: { x: number; y: number; z: number };
  color: string;
}

const poemColorPalette = [
  '#FF5733', // bright orange/red
  '#FFC300', // bright yellow
  '#DAF7A6', // light green
  '#FF33FF', // magenta
  '#33FFF6', // aqua
  '#FF8C00', // dark orange
  '#FF0055', // hot pink
  '#00FF7F', // spring green
  '#00FFFF', // cyan
  '#FFFF00', // yellow
];

function getRandomColor(): string {
  return poemColorPalette[Math.floor(Math.random() * poemColorPalette.length)];
}

// A simple function to set random positions in a range
function getRandomPosition(
  baseYear: number,
  poemYear: number
): { x: number; y: number; z: number } {
  // e.g., let’s factor the difference in years to cluster them slightly
  const yearDiff = poemYear - baseYear;
  // shift a bit in x-y-z space
  const spread = 10; // maximum random offset

  return {
    x: yearDiff * 2 + (Math.random() * spread - spread / 2),
    y: Math.random() * spread - spread / 2,
    z: Math.random() * spread - spread / 2,
  };
}

export function getRelatedPairs(): Array<[number, number]> {
  const pairs: Array<[number, number]> = [];

  for (let i = 0; i < poems.length; i++) {
    for (let j = i + 1; j < poems.length; j++) {
      const poemA = poems[i];
      const poemB = poems[j];

      const dateClose = Math.abs(poemA.date - poemB.date) <= 1;
      const themeOverlap = poemA.themes.some((theme) =>
        poemB.themes.includes(theme)
      );

      if (dateClose || themeOverlap) {
        pairs.push([poemA.number, poemB.number]);
      }
    }
  }

  return pairs;
}

